<h3>
Learning is the process of acquiring new understanding, knowledge, behaviors, skills, values, attitudes,
and preferences.[1] The ability to learn is possessed by humans, animals, and some machines; there is also
evidence for some kind of learning in certain plants.[2] Some learning is immediate, induced by a single event
(e.g. being burned by a hot stove), but much skill and knowledge accumulate from repeated experiences.
 The changes induced by learning often last a lifetime, and it is hard to distinguish learned material that seems to be "lost"
from that which cannot be retrieved.[3]
Human learning starts at birth (it might even start before[4]) and
continues until death as a consequence of ongoing interactions between people and their environment.
The nature and processes involved in learning are studied in many fields, including educational psychology,
neuropsychology, experimental psychology, and pedagogy. Research in such fields has led to the identification of various sorts of learning. For example,
learning may occur as a result of habituation, or classical conditioning, operant conditioning or as a result
of more complex activities such as play, seen only in relatively intelligent animals.[5][6] Learning may occur consciously or without conscious awareness. Learning that an aversive event can't be avoided nor escaped may result in a condition called learned helplessness.[7] There is evidence for human behavioral learning prenatally, in which habituation has been observed as early as 32 weeks into gestation, indicating that the central nervous system is sufficiently developed and primed for learning and memory to occur very early on in development.[8]
